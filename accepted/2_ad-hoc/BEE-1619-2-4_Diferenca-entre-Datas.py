monthdays = [31, 59, 90, 120, 151, 181, 212, 243, 273, 304, 334, 365]

for n in range(int(input())):
    date1, date2 = input().split()
    y1, m1, d1 = map(int, date1.split('-'))
    y2, m2, d2 = map(int, date2.split('-'))

    b1 = bissex[y1 - (1 if m1 < 2 else 0)]
    b2 = bissex[y2 - (1 if m2 < 2 else 0)]

    total1 = (y1 - 1) * 365 + monthdays[m1 - 1] + d1 + b1
    total2 = (y2 - 1) * 365 + monthdays[m2 - 1] + d2 + b2

    print(abs(total2 - total1))
